    public Quiz_result( String StudentID) {
        initComponents();
        try{
        ResultSet rs=select.getData("select *from student where StudentID='"+StudentID+"'");
            while(rs.next())
            {
            String cID=rs.getString(2);
            String Cname=rs.getString(3);
            String Sname=rs.getString(4);
            String Obt=rs.getString(9);
            
            in_cID.setText(cID);
            in_sID.setText(StudentID);
            in_cname.setText(Cname);
            in_sname.setText(Sname);
            Obtmarks.setText(Obt);
            
            int markss= Integer.parseInt(Obt);
            jProgressBar1.setValue(markss);
            jTextArea1.setLineWrap(true); // Enable text wrapping
            jTextArea1.setWrapStyleWord(true); 
            if(markss <= 3){
            jTextArea1.setText("Great job finishing your course! Your quiz scores are a bit low, but no worriesâ€”let's work together to improve them. Keep up the good effort!");
            }
            else if(markss > 3 && markss <= 7 ){
            jTextArea1.setText("Great job on finishing your course! While you've completed it, your quiz scores could use a bit of improvement. Take some time to review and boost your scores for an even better grasp of the material. Keep up the good work!");
            }
            else if(markss > 7){
            jTextArea1.setText("Congratulations on completing your course on our platform! Your exceptional quiz scores showcase your dedication and understanding of the material. Great job!");
            }
            }
            
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
        
    }


